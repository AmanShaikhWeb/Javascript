// while loop

let index = 0;
while (index <= 100) {
    console.log(`The Total Number is ${index}`);
    index++;
}

let superArray = ["IronMan", "Captain-America", "Hulk", "Spider-Man","Thor"]
let arr = 0
while (arr < superArray.length) {
    console.log(`superhero is ${superArray[arr]}`);
    arr++
}

